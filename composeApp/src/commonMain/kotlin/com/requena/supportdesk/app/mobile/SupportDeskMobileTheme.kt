@Composable
fun SupportDeskMobileTheme(
    content: @Composable () -> Unit,
) {
    val colorScheme = MaterialTheme.colorScheme

    MaterialTheme(
        colorScheme = colorScheme,
        typography = MobileTypography,
        shapes = MobileShapes,
    ) {
        Surface(
            color = colorScheme.background,
            content = content,
        )
    }
}
